package com.aula11;

public class VariaveisChar {

	public static void main(String[] args) {
		
		
		// Printa caracteres
		char o = 'o';
		char i = 'i';
		
		System.out.println("" + o + i + "\n");
		
		
		// printa numerais - tabela ascii
		char h = 'h';
		char e = 'e';
		char y = 'y';
		
		System.out.println(h + e + y + "\n");
		
		// exemplo unicode
		char interrogacao = 63;
		
		System.out.println("" + interrogacao);

	}

}
