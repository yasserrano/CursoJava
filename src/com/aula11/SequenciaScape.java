package com.aula11;

public class SequenciaScape {

	public static void main(String[] args) {
		
		//tab
		System.out.println("oi" + "\t" + "oi"); 
		
		//backspace
		System.out.println("oi" + "\b" + "oi");
		
		//nova linha
		System.out.println("oi" + "\n");
		
		//avanço de pagina
		System.out.println("oi" + "\f" + "oi");
		
		//aspas simples
		System.out.println("oi" + "\'" + "oi");
		
		//aspas duplas
		System.out.println("oi" + "\"" + "oi");
		
		//barra invertida
		System.out.println("oi" + "\\" + "oi");
	
	}

}
